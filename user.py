# user class
class User:
	def __init__(self):
		pass

	def set_name(self, name):
		self.name=name

	def get_name(self):
		return self.name

	def set_pwd(self, pwd):
		self.pwd=pwd

	def get_pwd(self):
		return self.pwd

	def set_email(self,email):
		self.email=email

	def get_email(self):
		return self.email

	def set_age(self,age):
		self.age=age

	def get_age(self):
		return self.age

	def set_phone(self,phone):
		self.phone=phone

	def get_phone(self):
		self.phone

	def set_sex(self,sex):
		self.sex=sex

	def get_sex(self):
		return self.sex